/// This is optional, if not set
/// The widget will use your design for the button
///  [default] value is [RippleButtonType.none]
/// Note: Some [RippleButtonStyles] may not be applied if using [RippleButtonType]
enum RippleButtonType {
  YELLOW,
  AMBER,
  WHITE_TRANSLUCENT,
  BLUE_TRANSLUCENT,
  GREEN,
  RED,
  NONE,
}
